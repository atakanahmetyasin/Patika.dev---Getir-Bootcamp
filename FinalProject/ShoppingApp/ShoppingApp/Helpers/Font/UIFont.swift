import UIKit

extension UIFont {
    static func openSansSemiBold(size: CGFloat) -> UIFont? {
        return UIFont(name: "OpenSans-SemiBold", size: size)
    }
    
    static func openSansBold(size: CGFloat) -> UIFont? {
        return UIFont(name: "OpenSans-Bold", size: size)
    }
}
